// we cant build everything from 0 so we always have to use some modules that are already built and ready to use. These modules are called node modules. Node modules are packages of code that can be reused in your own projects. They can be installed using npm (Node Package Manager) or yarn, which are package managers for JavaScript.

// ex : const express = require('express'); // express is a popular node module for building web applications
// ex : const eventemitter = require('events'); // events is a built-in node module for handling events

const http = require('http'); 

// we can use request or get method to make a request to a website. The difference is that request method allows us to send data to the server, while get method only retrieves data from the server. In this case, we are using get method to retrieve data from google.com, and also there is no need to do req.end() when using get method, but we have to do it when using request method.

// const req = http.request('http://google.com', (res) => { 
const req = http.get('http://google.com', (res) => { 
    // Create an empty array to store incoming chunks
    const chunks = [];

    // Push each piece of data into our array as it arrives
    res.on('data', (chunk) => { 
        chunks.push(chunk);
    }); 

    // This runs ONLY when the entire webpage has finished downloading
    res.on('end', () => { 
        // Combine all chunks and convert them into a readable string
        const fullResponseBody = Buffer.concat(chunks).toString();
        
        console.log("=== FULL WEBPAGE RECEIVED ===");
        console.log(fullResponseBody); 
    }); 
}); 

req.end(); 
